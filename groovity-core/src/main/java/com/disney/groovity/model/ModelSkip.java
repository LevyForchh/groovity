package com.disney.groovity.model;

import static java.lang.annotation.ElementType.FIELD;
import static java.lang.annotation.ElementType.METHOD;
import static java.lang.annotation.ElementType.TYPE;
import static java.lang.annotation.RetentionPolicy.RUNTIME;

import java.lang.annotation.Inherited;
import java.lang.annotation.Retention;
import java.lang.annotation.Target;
/**
 * Annotation to tell Model to skip a given property on each/copy, handy for breaking circular references or avoiding expensive dynamic lookups
 * unless explicitly called upon
 * 
 * @author Alex Vigdor
 */
@Retention(RUNTIME)
@Inherited
@Target({FIELD, METHOD, TYPE})
public @interface ModelSkip {
	String[] value() default {};
}
